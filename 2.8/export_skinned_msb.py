def normalize(bw):
	s = 0
	for b in bw:
		s+=b
	for i in range(len(bw)):
		bw[i] /= s
		bw[i] = int(bw[i]*255)

# not particularly fast, remaps bone indices to fit the required bone count
def recalculate_bone_indices(bi, bw, bsi_part):
	for i in range(len(bi)):
		bi[i] = (bsi_part.index(bi[i])) if (bw[i] != 0) else (0)
	return bi

